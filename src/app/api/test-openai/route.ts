import { NextResponse } from "next/server";
import { auth } from "@clerk/nextjs/server";

export async function GET() {
  try {
    const { userId } = await auth();
    
    if (!userId) {
      return NextResponse.json(
        { error: "Unauthorized" },
        { status: 401 }
      );
    }

    const hasApiKey = !!process.env.OPENAI_API_KEY;
    const apiKeyLength = process.env.OPENAI_API_KEY?.length || 0;
    const apiKeyPrefix = process.env.OPENAI_API_KEY?.substring(0, 7) || "none";

    return NextResponse.json({
      hasApiKey,
      apiKeyLength,
      apiKeyPrefix: `${apiKeyPrefix}...`,
      message: hasApiKey 
        ? "OpenAI API key is configured" 
        : "OpenAI API key is NOT configured. Please check your .env.local file and restart the dev server.",
    });
  } catch (error: any) {
    return NextResponse.json(
      { error: error?.message || "Internal server error" },
      { status: 500 }
    );
  }
}
