// Function to get syllabus topics for a specific class and board
export function getSyllabusTopics(classValue: string, board: string) {
  const classSyllabus = syllabusKnowledge[classValue as keyof typeof syllabusKnowledge];
  if (!classSyllabus) return null;
  
  const boardSyllabus = classSyllabus[board as keyof typeof classSyllabus];
  if (!boardSyllabus) return null;
  
  return boardSyllabus;
}

// Function to check if a topic might be out of syllabus
export function isOutOfSyllabus(message: string, classValue: string, board: string): boolean {
  const syllabus = getSyllabusTopics(classValue, board);
  if (!syllabus) return false; // If we can't determine, don't block
  
  const lowerMessage = message.toLowerCase();
  
  // Advanced topics that are typically not in the syllabus
  const advancedTopics = [
    "quantum mechanics", "relativity", "string theory", "black holes", "dark matter",
    "organic synthesis", "polymer chemistry advanced", "nuclear physics advanced",
    "calculus advanced", "differential equations advanced", "linear algebra advanced",
    "molecular biology advanced", "genetic engineering advanced", "biotechnology advanced"
  ];
  
  // Check for clearly advanced topics
  for (const topic of advancedTopics) {
    if (lowerMessage.includes(topic)) {
      return true;
    }
  }
  
  // For now, we'll let the AI decide based on the strict prompt
  // This is a basic check - the main filtering happens in the AI prompt
  return false;
}
